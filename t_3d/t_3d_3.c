#include "t_3d.h"

int				color_t_3d(t_3d a)
{
	return ((int)a.x * 65536 + (int)a.y * 256 + (int)a.z);
}

t_3d			t_3d_pair_mul(t_3d a, t_3d b)
{
	a.x = a.x * b.x;
	a.y = a.y * b.y;
	a.z = a.z * b.z;
	;
	return (a);
}
